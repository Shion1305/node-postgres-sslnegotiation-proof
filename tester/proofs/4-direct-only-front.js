// PROOF 4 — Reproduce the PR's exact claim with a direct-SSL-only endpoint.
//
// PostgreSQL 17 itself accepts BOTH negotiation styles, so to demonstrate that
// `direct` is genuinely a different protocol we put a TLS-ONLY listener in front
// of Postgres. It speaks raw TLS immediately (ALPN "postgresql") and forwards
// the decrypted stream to the real server.
//
//   * A direct client sends a TLS ClientHello first  -> handshake succeeds -> query runs.
//   * A traditional client sends 8 plaintext SSLRequest bytes first; to a TLS-only
//     endpoint those look like a malformed TLS record -> the connection is reset
//     (ECONNRESET) -> exactly the behavior the PR describes.
const tls = require('tls')
const net = require('net')
const fs = require('fs')
const { Client } = require('pg')
const { env, logResult } = require('./lib')

// Key/cert for the front are generated by run.sh with openssl and passed in.
const KEY = fs.readFileSync(process.env.FRONT_KEY || '/app/front.key')
const CERT = fs.readFileSync(process.env.FRONT_CERT || '/app/front.crt')

function startDirectOnlyFront() {
  return new Promise((resolve) => {
    const server = tls.createServer(
      { key: KEY, cert: CERT, ALPNProtocols: ['postgresql'] },
      (tlsSocket) => {
        const upstream = net.connect(env.port, env.host)
        tlsSocket.pipe(upstream)
        upstream.pipe(tlsSocket)
        tlsSocket.on('error', () => upstream.destroy())
        upstream.on('error', () => tlsSocket.destroy())
      }
    )
    // Swallow handshake errors (a traditional client's plaintext bytes trigger one).
    server.on('tlsClientError', () => {})
    server.listen(0, '127.0.0.1', () =>
      resolve({ port: server.address().port, close: () => new Promise((r) => server.close(r)) })
    )
  })
}

module.exports = async function proofDirectOnlyFront() {
  const front = await startDirectOnlyFront()

  // Direct client -> should connect through the TLS-only front.
  let directOk = false
  let directErr = null
  try {
    const c = new Client({
      host: '127.0.0.1',
      port: front.port,
      user: env.user,
      database: env.database,
      ssl: { rejectUnauthorized: false },
      sslnegotiation: 'direct',
    })
    await c.connect()
    const r = await c.query('SELECT 1 AS ok')
    await c.end()
    directOk = r.rows[0].ok === 1
  } catch (e) {
    directErr = e.code || e.message
  }
  logResult('Direct client succeeds against a direct-SSL-only endpoint', directOk, [
    directErr ? `error: ${directErr}` : 'connected + SELECT 1 ok',
  ])

  // Traditional client -> should be rejected (it sends plaintext SSLRequest
  // first, which a TLS-only endpoint cannot parse).
  let tradRejected = false
  let tradOutcome = 'connected (unexpected)'
  try {
    const c = new Client({
      host: '127.0.0.1',
      port: front.port,
      user: env.user,
      database: env.database,
      ssl: { rejectUnauthorized: false },
      sslnegotiation: 'postgres',
      connectionTimeoutMillis: 5000,
    })
    await c.connect()
    await c.end()
  } catch (e) {
    tradRejected = true
    tradOutcome = `rejected with ${e.code || e.message}`
  }
  logResult('Traditional client is rejected by the direct-SSL-only endpoint', tradRejected, [
    tradOutcome,
  ])

  await front.close()
  return directOk && tradRejected
}
